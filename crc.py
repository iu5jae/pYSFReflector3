def crc8(inp, l):
  crc = 0
  for i in range(l):
    crc = CRC8_TABLE[crc ^ inp[i]]
  return crc

def addCCITT162(inp, l):
  crc8 = [0] * 2
  crc16 = 0

  for i in range(l-2): 
    crc16 = (crc8[0] << 8) ^ CCITT16_TABLE2[crc8[1] ^ inp[i]]
    crc8[0] = crc16 & 0x00FF
    crc8[1] = (crc16 >> 8) & 0x00FF

  
  crc16 = ~crc16;
  crc8[0] = crc16 & 0x00FF
  crc8[1] = (crc16 >> 8) & 0x00FF
 
  inp[l - 1] = crc8[0]
  inp[l - 2] = crc8[1]
  
  

def checkCCITT162(inp, l):
  crc8 = [0] * 2
  crc16 = 0

  for i in range(l-2): 
    crc16 = (crc8[0] << 8) ^ CCITT16_TABLE2[crc8[1] ^ inp[i]]
    crc8[0] = crc16 & 0x00FF
    crc8[1] = (crc16 >> 8) & 0x00FF
  
  crc16 = ~crc16;
  crc8[0] = crc16 & 0x00FF
  crc8[1] = (crc16 >> 8) & 0x00FF
 
  return (crc8[0] == inp[l - 1]) and (crc8[1] == inp[l - 2])

def addCRC(inp, l):
  crc = 0

  for i in range(l):
    crc = (crc + inp[i]) & 0xFF
  
  return crc




